char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int i;
	int tmp;

	tmp = n;
	i = 0;
	while ((src[i] != '\0') && (i < tmp))
	{
		dest[i] = src[i];
		i++;
	}
	while ((dest[i] != '\0') && (i < tmp))
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}

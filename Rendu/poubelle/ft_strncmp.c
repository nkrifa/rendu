int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int i;
	int tmp;

	tmp = n;
	i = 0;
	while (i != tmp)
	{
		while (s1[i] == s2[i] && s1[i] && s2[i])
		{
			i++;
		}
	}
	return (s1[i] - s2[i]);

}
